/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */

  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */
	if(__HAL_UART_GET_FLAG(&huart3, UART_FLAG_IDLE) != RESET)   // 空闲中断的标志位
    {
        HAL_UART_DMAStop(&huart3);                               //停止接收
        rx_cnt = MAX_SIZE - __HAL_DMA_GET_COUNTER(&hdma_usart3_rx);    // 计算接收的数据长度
//		if(net_connect==1)
//			printf("%s", rx_buff);
		
		ptr = strstr((char *)rx_buff, "IPD,");				//搜索“IPD”头
        if(ptr != NULL)											//如果没找到，可能是IPD头的延迟，还是需要等待一会，但不会超过设定的时间
        {
			printf("%s", rx_buff);
            ptr = strchr(ptr, ':');							//找到':'
            if(ptr != NULL)
            {
                ptr++;
                xQueueSendFromISR(hreve_queue,((unsigned char *)(&ptr)), pdFALSE);
				ptr = NULL;
            }

        }
		
		
		if(net_connect == 1)
        {
            if((strstr((const char *)rx_buff, "link is not valid") != NULL))		//如果检索到关键词
            {
                ESP8266_Clear();									//清空缓存
                printf("网络断开，准备重连\r\n");
                net_connect = 2;
				reconnect_num++;
            }

        }
		else{
				
			//"\r\nERROR\r\nCLOSED\r\n"
			if((strstr((const char *)rx_buff,"ERROR")!=NULL))
			{
				if((strstr((const char *)rx_buff,"CLOSED")!=NULL))
				{
					printf("连接服务器失败\r\n");
					ESP8266_Clear();									//清空缓存
					net_connect = 3;
				}
			
			}
		
		}
        HAL_UART_Receive_DMA(&huart3, rx_buff, 512);       // 开启DMA继续接收
        __HAL_UART_CLEAR_IDLEFLAG(&huart3);
//		xQueueSendToFront(hreve_queue,&rx_buff,hreve_queue_base);
    }
  /* USER CODE END USART3_IRQn 1 */
}

